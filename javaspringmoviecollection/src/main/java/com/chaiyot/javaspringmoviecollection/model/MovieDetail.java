package com.chaiyot.javaspringmoviecollection.model;

public class MovieDetail {

	private int actors_id;
	
	private String actor_name;
	
	private String moviename;
	
	private String description;
	
	private String release_year;
	
	private String role;
	
	private String image;
	
	private String category;
	
	private String actor_image;
	
	

	public String getActor_image() {
		return actor_image;
	}

	public void setActor_image(String actor_image) {
		this.actor_image = actor_image;
	}

	public int getActors_id() {
		return actors_id;
	}

	public void setActors_id(int actors_id) {
		this.actors_id = actors_id;
	}


	public String getActor_name() {
		return actor_name;
	}

	public void setActor_name(String actor_name) {
		this.actor_name = actor_name;
	}

	public String getMoviename() {
		return moviename;
	}

	public void setMoviename(String moviename) {
		this.moviename = moviename;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getRelease_year() {
		return release_year;
	}

	public void setRelease_year(String release_year) {
		this.release_year = release_year;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}
	
	
	
}
