package com.chaiyot.javaspringmoviecollection.model;



public class MovCategory {

	private int movies_categories_id;
	
	private int category_id;
	
	private String category;

	
	


	public int getMovies_categories_id() {
		return movies_categories_id;
	}

	public void setMovies_categories_id(int movies_categories_id) {
		this.movies_categories_id = movies_categories_id;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public int getCategory_id() {
		return category_id;
	}

	public void setCategory_id(int category_id) {
		this.category_id = category_id;
	} 
	
	
}
